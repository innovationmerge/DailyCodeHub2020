# iNNovationMerge

# Create and view the current file permissions in Linux from command line using ls command

# Syntax 
# ls [OPTIONS] filename

# Create file with current premissions
$ touch filename.txt

# View the current premissions
$ ls -ltr filename.txt

# Output : 
# -rw-r--r-- 1 ubuntu ubuntu 0 Jan 17 12:56 filename.txt
