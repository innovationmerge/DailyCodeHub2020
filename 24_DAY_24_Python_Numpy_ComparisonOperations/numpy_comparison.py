import numpy as np

x = np.array([2,4,6,8])     # 
print(x<3)                  # [True False False False]
print(x>3)                  # [False  True  True  True]
print(x<=3)                 # [ True False False False]
print(x>=3)                 # [False  True  True  True]
print(x!=3)                 # [ True  True  True  True]
print(x==3)                 # [False False False False]
