# Set Date and time in Linux

# Show current Data and time
date    # Thu Dec 10 19:21:45 IST 2020

# Set date time - Example 1 
sudo date -s "11 OCT 2020 14:00:00" # Sun Oct 11 14:00:00 IST 2020

# Set only date 
sudo date +%Y%m%d -s "20201112" # 20201112

# Set only time
sudo date +%T -s "10:13:13"     # 10:13:13

# Verify date and time 
date    # Thu Nov 12 10:13:35 IST 2020


