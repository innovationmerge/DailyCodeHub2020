# Python program to swap variables in one line

################ Multiple variables ##########
a = 1
b = 2
temp = a
a = b
b = temp
print("For loop output : ", a,b)
# For loop output : 2 1

################ One liner ##########
a = 1
b = 2
a, b = b, a
print("One liner output : ", a,b) 
# One liner output : 2 1




