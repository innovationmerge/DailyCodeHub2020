# Python program to get sorted and reversed list

list_data = [5, 4, 3, 2, 1]

sorted_list = sorted(list_data)
print(sorted_list)
# Output : [1, 2, 3, 4, 5]

reversed_list = list(reversed(sorted_list))
print(reversed_list)
# Output : [5, 4, 3, 2, 1]


