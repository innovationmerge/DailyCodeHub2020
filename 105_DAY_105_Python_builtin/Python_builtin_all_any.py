# Python program to evaluate all the boolean values in it

result = all([False, True, True])
print(result)
# Output: False

result = all([True, True, True])
print(result)
# Output: True

result = any([False, True, True])
print(result)
# Output: True

result = any([True, True, True])
print(result)
# Output: True

