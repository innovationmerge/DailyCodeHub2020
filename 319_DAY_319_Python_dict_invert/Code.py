# Python program to Invert Dictionary

test_dict = {"SL1" : 1, "SL2" : 2, "SL3" : 3, "SL4" : 4 }
inverted_dict = {v: k for k, v in test_dict.items()}
print(inverted_dict)


#Output : {1: 'SL1', 2: 'SL2', 3: 'SL3', 4: 'SL4'}








