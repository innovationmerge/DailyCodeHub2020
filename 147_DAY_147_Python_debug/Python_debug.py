# Debugging Python code using breakpoint()

def divide_func(a, b):
    breakpoint()
    result = a / b
    return result
  
print(divide_func(5, 0))

"""Commands for debugging
c - Continue Execution
n - Go to next line within same method
s - Go to next line in a called method
q - Quit debugging
"""

