# Python program to convert integer to unicode and vice versa

result = chr(165) + '2000'
print(result)
# Output: ¥2000

result = 'a' + chr(215) + 'b'
print(result)
# Output: a×b

result = ord('×')
print(result)
# Output: 215

result = ord('¥')
print(result)
# Output: 165

