// Program to explain Type System of Java script
//  typeof                               | Output

typeof 1                                // number
typeof "2"                              // string
typeof [1,2,3]                          // object
typeof {Bigdata: "Hadoop", 
        Programming: "Python"}          // object
typeof true                             // boolean
typeof (1 === 1)                        // boolean
typeof undefined                        // undefined
typeof null                             // object

// Double Equals Versus Triple Equals 
1 == "1"                                // true
1 === "1"                               // false

//Checking for Falsiness
!12                                     // false
